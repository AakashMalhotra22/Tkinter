from tkinter import*

root = Tk()
''' root is instance of class Tk() which produces window as output which is called object.
Now here Tk() class has attributes ex: title,etc and methods ex: Label, etc so as instance root has.
Now this instance root will create the object window as output of class Tk() which has attributes and methods
which you have assigned to it.
'''

root.title("Hello World")
root.geometry('400x100')


root.mainloop()


